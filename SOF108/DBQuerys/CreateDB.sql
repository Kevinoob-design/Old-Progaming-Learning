CREATE TABLE DEPARTAMENTOS
(
  ID_DEPARTAMENTO    int PRIMARY KEY,
  NOMBRE_DEPARTAMENTO  VARCHAR(30),
  ID_SUPERVISOR       int,
  ID_LOCACION      int
