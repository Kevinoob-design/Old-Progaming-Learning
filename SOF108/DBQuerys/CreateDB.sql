CREATE TABLE PUESTOS
(
  ID_PUESTO      VARCHAR(10 ) PRIMARY KEY ,
  TITULO_PUESTO   VARCHAR(35 ),
  SALARIO_MINIMO  int,
  SALARIO_MAXIMO  int
)


CREATE TABLE EMPLEADOS
(
  ID_EMPLEADO     int  PRIMARY KEY ,
  NOMBRE      VARCHAR(20),
  APELLIDO       VARCHAR(25),
  EMAIL           VARCHAR(25),
  NUMERO_TELEFONO    VARCHAR(20),
  FECHA_CONTRATO       DATEtime,
  ID_PUESTO          VARCHAR(10),
  SALARIO          int,
  COMISION  int,
  ID_SUPERVISOR      int,
  ID_DEPARTAMENTO   int
)


CREATE TABLE HISTORICO
(
  ID_EMPLEADO    int,
  FECHA_INICIO     DATEtime,
  FECHA_FIN       DATEtime,
  ID_PUESTO         VARCHAR(10 ),
  ID_DEPARTAMENTO  int
)


CREATE TABLE PAISES
(
  ID_PAIS    varCHAR(2)   PRIMARY KEY ,
  NOMBRE_PAIS  VARCHAR(40),
  ID_REGION     int 
 )


CREATE TABLE LOCACIONES
(
  ID_LOCACION     int  PRIMARY KEY ,
  DIRECCION  VARCHAR(40 ),
  CODIGO_POSTAL     VARCHAR(12 ),
  CIUDAD            VARCHAR(30 ),
  PROVINCIA  VARCHAR(25 ),
  ID_PAIS      varCHAR(2)
)


CREATE TABLE REGIONES
(
  ID_REGION    int   PRIMARY KEY ,
  NOMBRE_REGION  VARCHAR(25 )
)





